//
//	魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾜ｣魹ｽ	魹ｽﾈｴ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//
//	魹ｽ魹ｽﾚｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ	魹ｽ魹ｽ
//
//	魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ	REV_OK-魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ		REV_WAIT-魹ｽ魹ｽ魹ｽﾕｳ魹ｽﾊｱﾎｴ魹ｽ魹ｽ魹ｽ
//
//	ﾋｵ魹ｽ魹ｽ魹ｽ魹ｽ		ﾑｭ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾃｼ魹ｽ魹ｽ魹ｽﾇｷ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//==========================================================
_Bool ESP8266_WaitRecive(void)
{

	if(esp8266_cnt == 0) 							//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾕｼ魹ｽ魹ｽ魹ｽﾎｪ0 魹ｽ魹ｽﾋｵ魹ｽ魹ｽﾃｻ魹ｽﾐｴ魹ｽ魹ｽﾚｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾐ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｱ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
		return REV_WAIT;
		
	if(esp8266_cnt == esp8266_cntPre)				//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｻ魹ｽﾎｵ魹ｽﾖｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾍｬ魹ｽ魹ｽ魹ｽ魹ｽﾋｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
	{
		esp8266_cnt = 0;							//魹ｽ魹ｽ0魹ｽ魹ｽ魹ｽﾕｼ魹ｽ魹ｽ魹ｽ
			
		return REV_OK;								//魹ｽ魹ｽ魹ｽﾘｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾉｱ魹ｽﾖｾ
	}
		
	esp8266_cntPre = esp8266_cnt;					//魹ｽ魹ｽﾎｪ魹ｽ魹ｽﾍｬ
	
	return REV_WAIT;								//魹ｽ魹ｽ魹ｽﾘｽ魹ｽ魹ｽ魹ｽﾎｴ魹ｽ魹ｽﾉｱ魹ｽﾖｾ

}

//==========================================================
//	魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾆ｣魹ｽ	ESP8266_SendCmd
//
//	魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾜ｣魹ｽ	魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//
//	魹ｽ魹ｽﾚｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ	cmd魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//				res魹ｽ魹ｽ魹ｽ魹ｽﾒｪ魹ｽ魹ｽ魹ｽﾄｷ魹ｽ魹ｽ魹ｽﾖｸ魹ｽ魹ｽ
//
//	魹ｽ魹ｽ魹ｽﾘｲ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ	0-魹ｽﾉｹ魹ｽ	1-ﾊｧ魹ｽ魹ｽ
//
//	ﾋｵ魹ｽ魹ｽ魹ｽ魹ｽ		
//==========================================================
_Bool ESP8266_SendCmd(char *cmd, char *res)
{
	
	unsigned char timeOut = 200;

	Usart_SendString(USART2, (unsigned char *)cmd, strlen((const char *)cmd));
	
	while(timeOut--)
	{
		if(ESP8266_WaitRecive() == REV_OK)							//魹ｽ魹ｽ魹ｽ魹ｽﾕｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
		{
			if(strstr((const char *)esp8266_buf, res) != NULL)		//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾘｼ魹ｽ魹ｽ魹ｽ
			{
				ESP8266_Clear();									//魹ｽ魹ｽﾕｻ魹ｽ魹ｽ魹ｽ
				
				return 0;
			}
		}
		
		DelayXms(10);
	}
	
	return 1;

}

