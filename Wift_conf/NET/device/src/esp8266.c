//				魹ｽ魹ｽESP8266魹ｽﾄｷ魹ｽ魹ｽﾘｸ魹ｽﾊｽﾎｪ	"+IPD,x:yyy"	x魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾝｳ魹ｽ魹ｽﾈ｣魹ｽyyy魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
//==========================================================
unsigned char *ESP8266_GetIPD(unsigned short timeOut)
{

	char *ptrIPD = NULL;
	
	do
	{
		if(ESP8266_WaitRecive() == REV_OK)								//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
		{
			ptrIPD = strstr((char *)esp8266_buf, "IPD,");				//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽIPD魹ｽ魹ｽﾍｷ
			if(ptrIPD == NULL)											//魹ｽ魹ｽ魹ｽﾃｻ魹ｽﾒｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽIPDﾍｷ魹ｽ魹ｽ魹ｽﾓｳﾙ｣魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｪ魹ｽﾈｴ魹ｽﾒｻ魹ｽ皀ｬ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ盖ｬ魹ｽ魹ｽ魹ｽ雜ｨ魹ｽ魹ｽﾊｱ魹ｽ魹ｽ
			{
				//UsartPrintf(USART_DEBUG, "¥"IPD¥" not found¥r¥n");
			}
			else
			{
				ptrIPD = strchr(ptrIPD, ':');							//魹ｽﾒｵ魹ｽ':'
				if(ptrIPD != NULL)
				{
					ptrIPD++;
					return (unsigned char *)(ptrIPD);
				}
				else
					return NULL;
				
			}
		}
		
		DelayXms(5);													//魹ｽ魹ｽﾊｱ魹ｽﾈｴ魹ｽ
	} while(timeOut--);
	
	return NULL;														//魹ｽ魹ｽﾊｱ魹ｽ魹ｽﾎｴ魹ｽﾒｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾘｿ魹ｽﾖｸ魹ｽ魹ｽ

}

