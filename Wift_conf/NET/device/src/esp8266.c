		Usart_SendString(USART2, data, len);		//魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ雎ｸ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
